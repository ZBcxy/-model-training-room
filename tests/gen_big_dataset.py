def expand():
    """对每个模板生成轻微变体，扩充到目标数量"""
    all_data = []
    seen = set()

    for topic, templates in DATA.items():
        for q, a in templates:
            # Original
            key = q[:30]
            if key not in seen:
                seen.add(key)
                all_data.append({"messages": [{"role":"user","content":q},{"role":"assistant","content":a}]})

            # Variants with different question phrasing
            prefixes = ["请问", "想问一下", "请教一个问题：", "你好，", "", "帮帮忙，"]
            for _ in range(2):
                p = random.choice(prefixes)
                vq = p + q if p else q
                key2 = vq[:30]
                if key2 not in seen:
                    seen.add(key2)
                    all_data.append({"messages": [{"role":"user","content":vq.strip()},{"role":"assistant","content":a}]})

    random.shuffle(all_data)
    return all_data


def main():
    data = expand()
    print(f"✅ 生成 {len(data)} 条数据")

    # Stats
    lengths = [sum(len(m["content"]) for m in item["messages"]) for item in data]
    print(f"   平均长度: {sum(lengths)/len(lengths):.0f} 字符")
    print(f"   最短: {min(lengths)} / 最长: {max(lengths)}")

    with open(OUTPUT / "data.json", "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    with open(OUTPUT / "meta.json", "w", encoding="utf-8") as f:
        json.dump({"name":"demo-2k-zh","record_count":len(data),"topics":list(DATA.keys()),"format":"sharegpt","description":"15个主题的中文对话训练数据，答案详细专业"}, f, ensure_ascii=False, indent=2)

    print(f"   保存: {OUTPUT / 'data.json'}")
    print(f"\n主题分布:")
    for topic in DATA:
        count = sum(1 for item in data if any(keyword in item["messages"][0]["content"] for keyword in [t[0][:10] for t in [("","")]]))
        print(f"   {topic}: {len(DATA[topic])} 个模板 × 3 变体 ≈ {len(DATA[topic])*3} 条")

if __name__ == "__main__":
    main()
